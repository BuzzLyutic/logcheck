package analyzer

// Rule определяет единую проверку достоверности, применяемую к каждому обнаруженному вызову журнала.
type Rule interface {
	// Name возвращает короткий идентификатор (например "lowercase").
	Name() string

	// Check валидирует lc и возвращает диагностическое сообщение когда правило нарушено. 
	// Пустая строка означает, что вызов выполнен нормально.
	Check(lc *LogCall) string
}
